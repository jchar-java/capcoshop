package com.jchar.capcoshop.clients;

public final class ClientParticulier extends Client {

	private String nom;
	
	private String prenom;
	
	public ClientParticulier(String nom, String prenom) {
		super();
		this.nom = nom;
		this.prenom = prenom;
	}

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	public String getPrenom() {
		return prenom;
	}

	public void setPrenom(String prenom) {
		this.prenom = prenom;
	}
}
